// Task 8 — Shorthand Operators



let score = 50;

console.log("Initial score is", score);

score += 10;
console.log("After += 10:", score);

score -= 5;
console.log("After -= 5:", score);

score *= 2;
console.log("After *= 2:", score);

score /= 5;
console.log("After /= 5:", score);

score %= 4;
console.log("After %= 4:", score);